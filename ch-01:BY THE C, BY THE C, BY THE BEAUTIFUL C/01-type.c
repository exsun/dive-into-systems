/* 
 Types in C
*/

#include <stdio.h>
#include <math.h>

int main(){
	int x;
	float y;
	char s;
	long l;
	long long lo;
	double d;

	printf("size of int %ld \n", sizeof(x));
	printf("size of float %ld \n", sizeof(y));
	printf("size of char %ld \n", sizeof(s));
	printf("size of long %ld \n", sizeof(l));
	printf("size of long long %ld \n", sizeof(lo));
	printf("size of double %ld \n", sizeof(d));


	return 0;
}
